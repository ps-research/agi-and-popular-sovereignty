# ─── Compute per-exemplar two-phase characteristics ────────────────

function compute_row(ts::TS)
    _, L = mean_series(ts, "leverage")
    L0 = L[1]
    dL = diff(L)
    nstep = length(dL)
    quarter = max(1, div(nstep, 4))
    early = -mean(@view dL[1:quarter])
    late  = -mean(@view dL[end-quarter+1:end])
    accel = (abs(early) > 1e-6 && abs(late) > 1e-6) ? late / early : NaN
    return (L0=L0, early=early, late=late, accel=accel)
end

rows = []
for ex in EXEMPLARS
    s = compute_row(TS_BY_NAME[ex.name])
    push!(rows, (name=ex.name, L0=s.L0, early=s.early, late=s.late, accel=s.accel))
end

