# ─── 3. Makie preview PDF + PNG ─────────────────────────────────────

function build_figure()
    nrows = length(rows)
    row_h = 38     # pixels per row — bigger to match larger fonts
    pad = 20
    fig_w = 720
    fig = Figure(size = (fig_w, row_h * (nrows + 1) + 2*pad))

    xmax = 92.0
    ax = Axis(fig[1, 1];
        limits = ((0, xmax), (0, nrows + 1)),
        xticksvisible = false, yticksvisible = false,
        xticklabelsvisible = false, yticklabelsvisible = false,
        xgridvisible = false, ygridvisible = false,
        topspinevisible = false, rightspinevisible = false,
        leftspinevisible = false, bottomspinevisible = false,
    )

    # Column anchor positions: trajectory + L̄(0) packed tightly; numeric columns
    # given enough separation that the wide headers ("Early-phase rate", etc.)
    # do not overlap.
    col_x = [2.5, 30.0, 50.0, 70.0, 90.0]
    col_align = [:left, :right, :right, :right, :right]
    headers = ["Trajectory", "L̄(0)", "Early-phase rate", "Late-phase rate", "Acceleration ratio"]

    header_yc = Float64(nrows) + 0.5
    poly!(ax, Point2f[(0, nrows), (xmax, nrows), (xmax, nrows + 1), (0, nrows + 1)];
          color = (:gray88, 0.7), strokewidth = 0)
    for i in eachindex(headers)
        text!(ax, col_x[i], header_yc; text = headers[i], font = :bold,
              fontsize = 14, color = :gray10, align = (col_align[i], :center))
    end

    for (idx, r) in enumerate(rows)
        y_low  = Float64(nrows) - idx
        y_high = y_low + 1
        yc = (y_low + y_high) / 2
        color = TRAJECTORY_COLORS[r.name]
        poly!(ax, Point2f[(0, y_low), (xmax, y_low), (xmax, y_high), (0, y_high)];
              color = (color, 0.18), strokewidth = 0)
        sign_e = r.early >= 0 ? "+" : ""
        sign_l = r.late  >= 0 ? "+" : ""
        cells = [r.name,
                 @sprintf("%.3f", r.L0),
                 @sprintf("%s%.5f", sign_e, r.early),
                 @sprintf("%s%.5f", sign_l, r.late),
                 @sprintf("%.2f×", r.accel)]
        for i in eachindex(cells)
            text!(ax, col_x[i], yc; text = cells[i],
                  fontsize = 13, color = :black,
                  align = (col_align[i], :center),
                  font = i == 5 ? :bold : :regular)
        end
    end

    lines!(ax, [0, xmax], [nrows + 1, nrows + 1]; color = :black, linewidth = 1.0)
    lines!(ax, [0, xmax], [nrows,     nrows];     color = :black, linewidth = 0.7)
    lines!(ax, [0, xmax], [0,         0];         color = :black, linewidth = 1.0)

    return fig
end

